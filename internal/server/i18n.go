// translate returns the string for key in lang, falling back to English then the
// key itself.
func translate(lang, key string) string {
	if m, ok := translations[lang]; ok {
		if v, ok := m[key]; ok {
			return v
		}
	}
	if v, ok := translations[defaultLang][key]; ok {
		return v
	}
	return key
}

// langFromRequest reads the language cookie, defaulting to English.
func langFromRequest(r *http.Request) string {
	if c, err := r.Cookie(langCookie); err == nil && translations[c.Value] != nil {
		return c.Value
	}
	return defaultLang
}

// handleSetLang sets the language cookie and redirects back.
func (s *Server) handleSetLang(w http.ResponseWriter, r *http.Request) {
	lang := r.URL.Query().Get("lang")
	if translations[lang] == nil {
		lang = defaultLang
	}
	http.SetCookie(w, &http.Cookie{
		Name: langCookie, Value: lang, Path: "/", HttpOnly: false,
		Secure: s.CookieSecure, SameSite: http.SameSiteLaxMode, MaxAge: 365 * 24 * 3600,
	})
	back := r.Header.Get("Referer")
	if back == "" {
		back = "/console"
	}
	http.Redirect(w, r, back, http.StatusFound)
}
